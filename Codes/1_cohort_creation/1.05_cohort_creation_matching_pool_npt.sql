 --9. Exclude from WDSD data unpaid carers identified in the LA, GP and step 6 
INSERT INTO SAILW1429V.PEJE_GPMATCHINGPOOL_STEP3_NPT
SELECT DISTINCT A.*, YEARS_BETWEEN('2017-01-01', A.WOB) AGE_STUDYSTART
FROM SAILW1429V.PEJE_GPMATCHINGPOOL_STEP1_NPT A
WHERE A.ALF_PE NOT IN (
	-- 8. Exclude if in  GP or LA unpaid carers cohort
	SELECT DISTINCT (ALF_PE)  FROM 
	( SELECT ALF_PE FROM SAILW1429V.PEJE_DEDUP_GP_LA_NPT
		UNION ALL SELECT ALF_PE FROM SAILW1429V.PEJE_GP_UNPAIDCARERS_20190101_NPT
	) a
); 

