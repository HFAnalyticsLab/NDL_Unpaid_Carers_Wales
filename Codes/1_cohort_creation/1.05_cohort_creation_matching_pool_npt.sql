---------------------------------
--  Identified carers Neath Port Talbot
---------------------------------
/* - those identified IN GP DATA FROM 2017-01-01 TO 2022-06-30
 * - Alive on 2017-01-01
 * - Aged >= 18 on 2017-01-01
 * - Known sex (1 or 2)
 * - Resides inSwansea on event date (Also accounts for known deprivation) - LSOA codes in SAILW1429V.LB_LSOA_LA_LOOKUP
 * - Resident in Wales for one year prior to event date
 * - Registered at a SAIL GP for one year prior to event date
 * - ALF_STS_CD 1, 4 or 39
 */




/* Cleaning steps
 * STEP 1  
 * 1. WDSD geography START_DT <= 2022-06-30 and END_DT >= 2017-01-01 (have an LSOA within study period)
 * 2. LSOA_DESC in NPT
 * 3. Link to WDSD demographics
 * 4. Sex 1 or 2
 * 
 * STEP 2 (create cohort of GP identified Unpaid carers for exclusion)
 * 5. Link to WLGP (inner join)
 *  -	ALF_STS_CD 1, 4 or 39
 *  -	Unpaid carer code with EVENT_DT during LSOA period
 *  -   GP carer code occurred whilst living at LSOA already selected
 *  -   GP carer code occurred within study period
 * 
 * STEP 3 (wash out period of 2 years for exclusion) 
 * 6. Link to WLGP (inner join)
 * 7. Create cohort with unpaid carer read code in the 2 year period before the study start 
 * 
 * STEP 4  
 * 8. Link STEP 1 to WLGP (inner join)
 * 9. Exclude those in steps 2 and 3 and from LA
 * 
 * STEP 5
 * 10. Link to ADDE
 * 11. Drop rows with DEATH_DT before EVENT_DT
 * 12. Drop rows under 14 at study start as they will not be 18 by 2022
 * 
 * STEP 6
 * 13. Link to CLEAN_ADD_WALES
 * 14. Keep where Welsh = 1
 * 15. Drop rows where WELSH_END_DATE < study start
 * 
 * Create washout period cohort
 * 16.Create lookup for washout period
 * - ALF_STS_CD 1, 4 or 39
 * - has carer read code 
 * - Unpaid carer code with EVENT_DT 2 year prior to study start
 * 
 * Create GP registration lookup table
 * 17. GP registration within WELSH address period
 * 
 * Create LSOA lookup table
 * 18. LS0A within WELSH address period
 * 
 * STEP 7
 * 19. Create deduplicated list of key columns with only ALFs
 */
DROP TABLE SAILW1429V.PEJE_GPMATCHINGPOOL_STEP1_NPT; 
DROP TABLE SAILW1429V.PEJE_GPMATCHINGPOOL_STEP2_NPT;
DROP TABLE SAILW1429V.PEJE_GP_UNPAIDCARERS_20190101_NPT;
DROP TABLE SAILW1429V.PEJE_GPMATCHINGPOOL_STEP3_NPT;
DROP TABLE SAILW1429V.PEJE_GPMATCHINGPOOL_STEP4_NPT;
