DROP TABLE SAILW1429V.PEJE_GPMATCHINGPOOL_NPT;



CREATE TABLE SAILW1429V.PEJE_GPMATCHINGPOOL_STEP1_NPT
(ALF_PE BIGINT
, START_DATE DATE
, END_DATE DATE
, LSOA2011_CD VARCHAR(10)
, WIMD INTEGER
, WIMD_DESC VARCHAR(50)
, RUC11CD VARCHAR(3)
, RUC11_DESC VARCHAR(50)
, LA_NAME VARCHAR(50)
, SEX INTEGER
, WOB DATE);

INSERT INTO SAILW1429V.PEJE_GPMATCHINGPOOL_STEP1_NPT
-- 3. Link to WDSD demographics
SELECT DISTINCT B.*, P.GNDR_CD AS SEX, P.WOB 
FROM(
	-- 2. LSOA_DESC in NPT
	SELECT A.*, R.RUC11CD, R.RUC11, L.LA_NAME
	FROM(
		-- 1. WDSD geography START_DT <= 2022-06-30 and END_DT >= 2017-01-01 (have an LSOA within study period)
		SELECT ALF_PE, START_DATE, END_DATE, LSOA2011_CD, WIMD_2019_QUINTILE, WIMD_2019_QUINTILE_DESC
		FROM SAIL1429V.WDSD_CLEAN_ADD_GEOG_CHAR_LSOA2011_20220704 G
		WHERE START_DATE <= '2022-06-30' AND END_DATE >= '2017-01-01'
	) A
	INNER JOIN SAILW1429V.LB_LSOA_LA_LOOKUP L
	ON A.LSOA2011_CD = L.LSOA_CD
	-- join ruc
	LEFT JOIN (SELECT LSOA11CD,RUC11CD,RUC11  FROM SAILREFRV.RURAL_URBAN_CLASS_2011_OF_LLSOAREAS_IN_ENG_AND_WAL) R
	ON R.LSOA11CD = A.LSOA2011_CD
	WHERE LA_NAME = 'Neath Port Talbot'
) B
LEFT JOIN SAIL1429V.WDSD_AR_PERS_20220704 P
ON B.ALF_PE = P.ALF_PE; 
-- Adding step 4 here goes beyond counting capabilities of SAIL, so create table with output of steps 1-3.



-- 4. Sex 1 or 2
DELETE FROM SAILW1429V.PEJE_GPMATCHINGPOOL_STEP1_NPT
WHERE SEX NOT IN(1,2); 

-- Remove those with no WOB
DELETE FROM SAILW1429V.PEJE_GPMATCHINGPOOL_STEP1_NPT
WHERE WOB IS NULL; 


DROP TABLE SAILW1429V.PEJE_GPMATCHINGPOOL_STEP2_NPT;
-- replaced codes to use dedup GP and LA codes


DROP TABLE SAILW1429V.PEJE_GP_UNPAIDCARERS_20190101_NPT;

CREATE TABLE SAILW1429V.PEJE_GP_UNPAIDCARERS_20190101_NPT
(ALF_PE BIGINT,
LSOA2011_CD VARCHAR(10),
WIMD INTEGER,
LA_NAME VARCHAR(100),
SEX INTEGER,
WOB DATE,
EVENT_DT DATE);
--7. Create cohort with unpaid carer read code in the 2 year period before the study start  ( doesnt really work for NPT due to wide range of possible index dates)
-- helps to reduce matching cohort size
INSERT INTO SAILW1429V.PEJE_GP_UNPAIDCARERS_20190101_NPT
SELECT DISTINCT A.ALF_PE , A.LSOA2011_CD, A.WIMD, A.LA_NAME, A.SEX, A.WOB , MAX(GP.EVENT_DT) LATEST_UNPAIDCARER_EVENT_DATE
FROM SAILW1429V.PEJE_GPMATCHINGPOOL_STEP1_NPT A
--6. Link to WLGP (inner join)
INNER JOIN SAIL1429V.WLGP_GP_EVENT_CLEANSED_20220301 GP
ON A.ALF_PE = GP.ALF_PE 
--  -	ALF_STS_CD 1, 4 or 39
WHERE GP.ALF_STS_CD IN (1, 4, 39) 
 --  -	Unpaid carer code with EVENT_DT during LSOA period
AND GP.EVENT_CD IN(SELECT READ_CODE FROM(SELECT READ_CODE 
	FROM SAILW1429V.LB_CARER_READ_CODES RC))  
-- GP carer code occurred within study period
AND GP.EVENT_DT >= '2015-01-01' AND GP.EVENT_DT < '2017-01-01'
GROUP BY A.ALF_PE , A.LSOA2011_CD, A.WIMD, A.LA_NAME, A.SEX, A.WOB ; 



-- Count of all ALFs to be excluded
SELECT COUNT (DISTINCT (ALF_PE))  FROM 
( SELECT ALF_PE FROM SAILW1429V.PEJE_DEDUP_GP_LA_NPT
	UNION ALL SELECT ALF_PE FROM SAILW1429V.PEJE_GP_UNPAIDCARERS_20190101_NPT
) a; 


